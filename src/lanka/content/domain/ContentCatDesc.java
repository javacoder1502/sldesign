package lanka.content.domain;

public class ContentCatDesc {
	
	String cat_name = "";
	String cat_desc = "";
	String image_prv_name="";
	
	public String getCat_name() {
		return cat_name;
	}
	
	public void setCat_name(String cat_name) {
		this.cat_name = cat_name;
	}
	
	public String getCat_desc() {
		return cat_desc;
	}
	
	public void setCat_desc(String cat_desc) {
		this.cat_desc = cat_desc;
	}
	
	public String getImage_prv_name() {
		return image_prv_name;
	}
	
	public void setImage_prv_name(String image_prv_name) {
		this.image_prv_name = image_prv_name;
	}
	
	
}
