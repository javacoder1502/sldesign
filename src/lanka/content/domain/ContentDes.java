package lanka.content.domain;

public class ContentDes {
	
	
	private String content_name;
	private String content_prv;
	private String code;
	private String pricetag;
	

	
	
	public String getContent_name() {
		return content_name;
	}
	public void setContent_name(String content_name) {
		this.content_name = content_name;
	}
	public String getContent_prv() {
		return content_prv;
	}
	public void setContent_prv(String content_prv) {
		this.content_prv = content_prv;
	}
	/**
	 * @return the code
	 */
	public String getCode() {
		return code;
	}
	/**
	 * @param code the code to set
	 */
	public void setCode(String code) {
		this.code = code;
	}
	/**
	 * @return the pricetag
	 */
	public String getPricetag() {
		return pricetag;
	}
	/**
	 * @param pricetag the pricetag to set
	 */
	public void setPricetag(String pricetag) {
		this.pricetag = pricetag;
	}
	

}
